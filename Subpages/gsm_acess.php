<?php
// Połączenie z bazą danych
$dbhost = "localhost";
$dbuser = "root";
$dbpass = "";
$dbname = "hurtownia_komputerowa";

$conn = mysqli_connect($dbhost, $dbuser, $dbpass, $dbname);

// sprawdzanie, czy połączenie się udało
if (!$conn) {
    die("Nie udało się połączyć z bazą danych: " . mysqli_connect_error());
}

// zapytanie SQL pobierające produkty z konkretnej tabeli
$sql = "SELECT * FROM magazyn WHERE rodzaj='akcesoria_gsm'";

// wykonanie zapytania i zapisanie wyników do zmiennej $result
$result = mysqli_query($conn, $sql);

// wyświetlenie wyników na stronie
if (mysqli_num_rows($result) > 0) {
    while ($row = mysqli_fetch_assoc($result)) {
        echo '<div class="products_list">';
        echo '<img class="product_img" src="data:image/jpeg;base64,'.base64_encode($row['zdjecie']).'"/>';
        echo '<h3 class="prod_h3">' . $row["producent"] . "</h3>";
        echo "<p>" . $row["nazwa"] . "</p>";
        echo "<p>Cena: " . $row["cena"] . " zł</p>";
        echo "<p>Dostępność: " . $row["dostepnosc"] . "</p>";
        echo "<button onclick='addToCart(" . $row["id"] . ")'>Dodaj do koszyka</button>";
        echo "</div>";
    }
} else {
    echo "Brak produktów w bazie danych.";
}

// zamknięcie połączenia z bazą danych
mysqli_close($conn);

?>
<!-- Kod JavaScript dodający produkty do koszyka -->
<script>
function addToCart(id) {
  var xhr = new XMLHttpRequest();
  xhr.open('POST', 'addToCart.php');
  xhr.setRequestHeader('Content-Type', 'application/x-www-form-urlencoded');
  xhr.onload = function() {
    if (xhr.status === 200 && xhr.responseText !== 'error') {
      alert('Produkt został dodany do koszyka!');
    } else {
      alert('Wystąpił błąd podczas dodawania produktu do koszyka.');
    }
  };
  xhr.send('id=' + id);
}
</script>

